// تحديد اللغة الافتراضية بناءً على لغة المتصفح
function getDefaultLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    
    if (browserLang.startsWith('ar')) {
        return 'ar';
    } else if (browserLang.startsWith('nl')) {
        return 'nl';
    } else {
        return 'en'; // اللغة الإنجليزية كلغة افتراضية إذا لم تكن اللغة العربية أو الهولندية
    }
}

// اللغة الحالية
let currentLanguage = getDefaultLanguage();

// تطبيق الترجمات على العناصر
function applyTranslations() {
    const elements = document.querySelectorAll('[data-lang-key]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-lang-key');
        if (translations[currentLanguage][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[currentLanguage][key];
            } else {
                element.textContent = translations[currentLanguage][key];
            }
        }
    });
    
    // تحديث اتجاه الصفحة بناءً على اللغة
    document.documentElement.lang = currentLanguage;
    document.documentElement.dir = currentLanguage === 'ar' ? 'rtl' : 'ltr';
    
    // تحديث زر اللغة النشط
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === currentLanguage) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// تغيير اللغة
function changeLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        applyTranslations();
    }
}

// تهيئة اللغة عند تحميل الصفحة
document.addEventListener('DOMContentLoaded', () => {
    // تطبيق الترجمات الأولية
    applyTranslations();
    
    // إضافة مستمعي الأحداث لأزرار تبديل اللغة
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            changeLanguage(btn.getAttribute('data-lang'));
        });
    });
});
